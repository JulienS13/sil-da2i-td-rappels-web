			<h2>Biographie</h2>
				<p>Scott est reconnu pour son style visuel très concentré et atmosphérique, qui a inspiré un grand nombre de réalisateurs1,2. Il a été nommé à trois reprises à l'oscar du meilleur réalisateur, ainsi qu'aux Golden Globes et BAFTA Awards. En 2003, il est anobli par la reine Élisabeth II pour services rendus aux arts britanniques, et en 2015, il reçoit un doctorat honorifique par le Royal College of Art de Londres.</p>

				<p>Il est le frère aîné du réalisateur Tony Scott (1944-2012) avec qui il a fondé la société de production Scott Free Productions en 1995, et ont reçu un BAFTA Award pour leur contribution remarquable au septième art. Il est le père de trois enfants, tous réalisateurs : Jake, Luke et Jordan (également romancière).</p>

				<p>Il est un réalisateur de films à succès tels que Gladiator, Hannibal, Robin des Bois ou encore Exodus: Gods and Kings, qui s'est particulièrement distingué dans le genre spatial-science-fiction, dans lequel il est le réalisateur le plus prolifique avec Alien, le huitième passager, Blade Runner, Prometheus et Seul sur Mars (son plus grand succès à ce jour). Son œuvre unique et pionnière poursuit sa prospection en 2017 avec son film Alien: Covenant.</p>

